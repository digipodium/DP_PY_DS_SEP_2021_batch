'''
python has 9 basic datatypes
- int
- float
- bool
- None
- str
- list
- tuple
- set
- dict
'''
a = 9012
print( a, type(a))

a = 129.8293
print( a, type(a))

a = True
print( a, type(a))

a = None
print( a, type(a))

a = 'Sam'
print( a, type(a))

a = [1,2,3]   # square brackets
print( a, type(a))

a = ('hey','hi','hello') # round brackets
print(a, type(a))

a = {3,2,1}   # curly brackets
print( a, type(a))

a = {'apple':'a fruit','brinjal':'a vegetable'}
print(a, type(a))